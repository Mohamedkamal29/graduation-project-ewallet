package com.graduation.ewallet.Model.Auth;

public class RegisterModel {


   private String token;

   private String balance;

   private String wallet_qr;

   private String name;

    private String email;

    private String phone;

    private String job;

    private String image;

    private String contact_qr;


    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public String getBalance() {
        return balance;
    }

    public void setBalance(String balance) {
        this.balance = balance;
    }

    public String getWallet_qr() {
        return wallet_qr;
    }

    public void setWallet_qr(String wallet_qr) {
        this.wallet_qr = wallet_qr;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getJob() {
        return job;
    }

    public void setJob(String job) {
        this.job = job;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getContact_qr() {
        return contact_qr;
    }

    public void setContact_qr(String contact_qr) {
        this.contact_qr = contact_qr;
    }
}
